# KEYS() METHOD

stu = {100: 'Harsh', 101: 'Sangam', 103:'Yadav'}

print("Original Dict: ")
print(stu)
print()

all_keys = stu.keys()
print(all_keys)
print(type(all_keys))
print()

keys_lst = list(all_keys)
print(keys_lst)
print(type(keys_lst))

print(keys_lst[0])
print(keys_lst[1])
print(keys_lst[2])

for k in keys_lst:
    print(k)

    




