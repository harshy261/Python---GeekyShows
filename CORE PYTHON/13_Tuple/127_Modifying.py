# MODIFYING TUPLE 


# To add :-
a = (10, 20, 30)
b = ('Harsh', 'Yadav')

tupl1 = a+b
print(tupl1)




# To delete :-
a = (10, 20, 30, 'Harsh', 'yadav')
print(a)

tupl2 = a[0:3]

print(tupl2)


# To Insert :- 
a = (10, 20, 30, 'Harsh', 'yadav')
c = (101, 102)
s1 = a[0:3]
s2 = a[2:1]

tup3 = s1+c+s2
print(tup3)










